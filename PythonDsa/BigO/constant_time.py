# O(1) - Constant Time Complexity
# This function has a time complexity of O(1) because the time taken to execute the function is constant and does not depend on the input size n.
# The function performs a fixed number of operations, regardless of the input size.
# The time complexity of this function is O(1) or constant time complexity.
def print_first_item(arr):
    print(arr[0])

arr = [1, 2, 3, 4, 5]
print_first_item(arr)
# Output: 1
# This function prints the first element of the input array arr.
# The time taken by the function to execute is constant and does not depend on the size of the input array.
# The time complexity of this function is O(1) or constant time complexity.
# Constant time complexity indicates that the time taken by the function to execute remains constant, regardless of the input size.
# The function performs a fixed number of operations, and the time taken to execute the function does not increase with the input size.