# O(n) - Linear Time Complexity
def print_items(n):
    for i in range(n):
        print(i)


print_items(5)

# O(n) - Linear Time Complexity
# This function has a time complexity of O(n) because the number of iterations of the loop is directly proportional to the input size n.
# As the value of n increases, the number of iterations of the loop also increases linearly.
# The time taken by the function to execute is directly proportional to the input size n.
# The time complexity of this function is O(n) or linear time complexity.